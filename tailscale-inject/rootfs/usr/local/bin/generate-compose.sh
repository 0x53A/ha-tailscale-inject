#!/usr/bin/env bash
set -euo pipefail

OPTIONS="/data/options.json"
COMPOSE_DIR="/data/compose"
COMPOSE_FILE="${COMPOSE_DIR}/docker-compose.yml"
ENTRYPOINT_SRC="/usr/local/bin/ts-entrypoint.sh"

mkdir -p "$COMPOSE_DIR"

# Read global auth key
GLOBAL_AUTH_KEY=$(jq -r '.auth_key // ""' "$OPTIONS")

# Read devices array
DEVICE_COUNT=$(jq '.devices | length' "$OPTIONS")

if [ "$DEVICE_COUNT" -eq 0 ]; then
    echo "[generate-compose] No devices configured"
    rm -f "$COMPOSE_FILE"
    exit 0
fi

# Copy entrypoint template
cp "$ENTRYPOINT_SRC" "${COMPOSE_DIR}/ts-entrypoint.sh"
chmod +x "${COMPOSE_DIR}/ts-entrypoint.sh"

# Generate Dockerfile.ts
cat > "${COMPOSE_DIR}/Dockerfile.ts" <<'DEOF'
FROM tailscale/tailscale:latest
RUN apk add --no-cache socat
COPY ts-entrypoint.sh /usr/local/bin/ts-entrypoint.sh
RUN chmod +x /usr/local/bin/ts-entrypoint.sh
ENTRYPOINT ["/usr/local/bin/ts-entrypoint.sh"]
DEOF

# Track reverse-mode published ports for conflict detection
declare -A USED_PORTS

# Start composing
cat > "$COMPOSE_FILE" <<'HEADER'
# Auto-generated by tailscale-inject — do not edit
services:
HEADER

VOLUMES_SECTION=""

for i in $(seq 0 $((DEVICE_COUNT - 1))); do
    NAME=$(jq -r ".devices[$i].name" "$OPTIONS")
    MODE=$(jq -r ".devices[$i].mode" "$OPTIONS")
    IP=$(jq -r ".devices[$i].ip" "$OPTIONS")
    DEV_AUTH_KEY=$(jq -r ".devices[$i].auth_key // \"\"" "$OPTIONS")
    PORTS_JSON=$(jq -c ".devices[$i].ports // []" "$OPTIONS")
    PORTS_COUNT=$(echo "$PORTS_JSON" | jq 'length')

    # Determine auth key (per-device overrides global)
    AUTH_KEY="${DEV_AUTH_KEY:-$GLOBAL_AUTH_KEY}"
    if [ -z "$AUTH_KEY" ]; then
        echo "[generate-compose] ERROR: No auth_key for device '$NAME' and no global auth_key set"
        exit 1
    fi

    # Validate ports present
    if [ "$PORTS_COUNT" -eq 0 ]; then
        echo "[generate-compose] ERROR: Device '$NAME' has no ports configured — ports are required"
        exit 1
    fi

    # Build FORWARD_PORTS env var: "631/tcp,9100/tcp,5353/udp"
    FORWARD_PORTS=""
    PUBLISH_LINES=""
    for j in $(seq 0 $((PORTS_COUNT - 1))); do
        PORT_SPEC=$(echo "$PORTS_JSON" | jq -r ".[$j]" | tr -d ' ')

        # Parse port/proto
        if [[ "$PORT_SPEC" == */* ]]; then
            PORT="${PORT_SPEC%%/*}"
            PROTO="${PORT_SPEC##*/}"
        else
            PORT="$PORT_SPEC"
            PROTO="tcp"
        fi

        [ -n "$FORWARD_PORTS" ] && FORWARD_PORTS+=","
        FORWARD_PORTS+="${PORT}/${PROTO}"

        # For reverse mode, publish ports on the host
        if [ "$MODE" = "reverse" ]; then
            PORT_KEY="${PORT}/${PROTO}"
            if [ -n "${USED_PORTS[$PORT_KEY]:-}" ]; then
                echo "[generate-compose] ERROR: Port ${PORT_KEY} used by both '${USED_PORTS[$PORT_KEY]}' and '$NAME'"
                exit 1
            fi
            USED_PORTS[$PORT_KEY]="$NAME"
            PUBLISH_LINES+="      - \"${PORT}:${PORT}/${PROTO}\"\n"
        fi
    done

    SERVICE_NAME=$(echo "ts-${NAME}" | tr '[:upper:]' '[:lower:]')
    VOLUME_NAME=$(echo "ts_state_${NAME}" | tr '[:upper:]' '[:lower:]' | sed 's/[^a-z0-9_]/_/g')

    cat >> "$COMPOSE_FILE" <<EOF
  ${SERVICE_NAME}:
    build:
      context: .
      dockerfile: Dockerfile.ts
    container_name: ${SERVICE_NAME}
    hostname: ${NAME}
    restart: unless-stopped
    environment:
      - TS_HOSTNAME=${NAME}
      - TS_AUTH_KEY=${AUTH_KEY}
      - TS_USERSPACE=true
      - FORWARD_TARGET=${IP}
      - FORWARD_PORTS=${FORWARD_PORTS}
      - FORWARD_MODE=${MODE}
    volumes:
      - ${VOLUME_NAME}:/var/lib/tailscale
EOF

    # Add port publishing for reverse mode
    if [ "$MODE" = "reverse" ] && [ -n "$PUBLISH_LINES" ]; then
        echo "    ports:" >> "$COMPOSE_FILE"
        echo -e "$PUBLISH_LINES" >> "$COMPOSE_FILE"
    fi

    VOLUMES_SECTION+="  ${VOLUME_NAME}:\n"

    echo "[generate-compose] Device '$NAME': mode=$MODE ip=$IP ports=$FORWARD_PORTS"
done

# Write volumes section
if [ -n "$VOLUMES_SECTION" ]; then
    echo "" >> "$COMPOSE_FILE"
    echo "volumes:" >> "$COMPOSE_FILE"
    echo -e "$VOLUMES_SECTION" >> "$COMPOSE_FILE"
fi

echo "[generate-compose] Wrote $COMPOSE_FILE with $DEVICE_COUNT device(s)"
